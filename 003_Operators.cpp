#include <iostream>
using namespace std;


int main() {
	int a=12,b=5;

	//Arithmetic
	cout<<"Arithmetic Operators\n\n";

	//Addition
	cout<<a<<" + "<<b<<" = "<<(a+b)<<endl;

	//Subtraction
	cout<<a<<" - "<<b<<" = "<<(a-b)<<endl;

	//Multiplication
	cout<<a<<" * "<<b<<" = "<<(a*b)<<endl;

	//Division
	cout<<a<<" / "<<b<<" = "<<(a/b)<<endl;

	//Modulus
	cout<<a<<" % "<<b<<" = "<<(a%b)<<endl;

	//Increment
	cout<<"a = "<<a<<"; ++a"<<" = "<<(++a)<<endl;

	//Decrement
	cout<<"b = "<<b<<"; --b"<<" = "<<(--b)<<endl;




	return 0;
}
